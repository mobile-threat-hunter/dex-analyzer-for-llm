static const size_t kValidDexLen = sizeof(kValidDex);

static int g_fail = 0;
static void check(bool cond, const char* what) {
    if (cond) {
        std::printf("  ok   %s\n", what);
    } else {
        std::printf("  FAIL %s\n", what);
        ++g_fail;
    }
}

// Run a mutation (captureless lambda → fn ptr) on a copy of the valid dex, verify.
static dexkit::ext::DexVerifyResult mutated(void (*fn)(std::vector<unsigned char>&)) {
    std::vector<unsigned char> b(kValidDex, kValidDex + kValidDexLen);
    fn(b);
    return dexkit::ext::VerifyDex(b.data(), b.size());
}

int main() {
    using dexkit::ext::VerifyDex;

    // 1. positive — the embedded dex is well-formed.
    check(VerifyDex(kValidDex, kValidDexLen).ok, "valid dex accepted");

    // 2. negative — crafted malformations must be rejected.
    check(!VerifyDex(nullptr, 0).ok, "null/empty rejected");
    check(!VerifyDex(kValidDex, 20).ok, "truncated header rejected");
    check(!mutated([](std::vector<unsigned char>& b) { b[0] = 'X'; }).ok,
          "bad magic rejected");
    // header field offsets: map_off@52, string_ids_size@56 (standard dex header).
    check(!mutated([](std::vector<unsigned char>& b) {
              b[56] = b[57] = b[58] = b[59] = 0xff;  // string_ids_size = huge
          }).ok, "out-of-range string_ids_size rejected");
    check(!mutated([](std::vector<unsigned char>& b) {
              b[52] = b[53] = b[54] = b[55] = 0xff;  // map_off = huge
          }).ok, "out-of-range map_off rejected");

    // 3. fuzz — the verifier is the first code to touch untrusted bytes; it must
    // never crash on arbitrary input (a segfault here kills the process → FAIL).
    uint64_t s = 0x9E3779B97F4A7C15ull;
    auto rnd = [&s]() -> uint32_t {
        s = s * 6364136223846793005ull + 1442695040888963407ull;
        return static_cast<uint32_t>(s >> 33);
    };
    int done = 0;
    for (int i = 0; i < 20000; ++i) {
        std::vector<unsigned char> b(kValidDex, kValidDex + kValidDexLen);
        int m = 1 + static_cast<int>(rnd() % 12);
        for (int j = 0; j < m; ++j) b[rnd() % b.size()] = static_cast<unsigned char>(rnd());
        size_t len = (i % 5 == 0) ? (1 + rnd() % b.size()) : b.size();  // also fuzz truncation
        auto r = VerifyDex(b.data(), len);  // must return cleanly, not crash
        (void)r;
        ++done;
    }
    check(done == 20000, "20000-iteration mutation fuzz completed (no crash)");

    std::printf(g_fail ? "FAILED (%d checks)\n" : "PASS\n", g_fail);
    return g_fail ? 1 : 0;
}
